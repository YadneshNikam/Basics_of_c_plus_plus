#include <iostream>
using namespace std;
int main() {
    int number;
    cout << "Enter an number: ";
    cin >> number;
    if (number > 0) {
        cout << "The number " << number << " is positive." << endl;
    } else if (number < 0) {
        cout << "The number " << number << " is negative." << endl;
    } else {
        cout << "The number is zero." << endl;
    }
}
/*Enter an number: 3
The number 3 is positive.*/
